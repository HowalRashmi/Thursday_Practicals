//write a java program to find if number is even or odd
class EvenOdd    //creating class
{
	public static void main(String args[])   //main method
	{
		int a=15;   //declaring variable
		if (a%2==0)  //if condition to check if no is even
		{
			System.out.println(a+" is an even Number");   //printing output
		}
		else
		{
			System.out.println(a+" is a odd Number");    //printing output
		}
	}
}
